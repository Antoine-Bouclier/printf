#include "ft_printf.h"

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	ft_printstr(char *str)
{
	int	i;

	i = 0;
	if (str == NULL)
	{
		ft_putstr(("(null)"));
		return (6);
	}
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
	return (i);
}

int	ft_printnbr(long int n, char *base)
{
	char	*nb;
	int		len;

	len = 0;
	nb = ft_itoa_base(n, base);
	len = ft_printstr(nb);
	free(nb);
	return (len);
}
